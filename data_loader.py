import yfinance as yf
import pandas as pd
import streamlit as st
import ta

@st.cache_data(ttl=3600)
def fetch_data(ticker, start_date, end_date):
    """
    Fetch historical stock data using yfinance.
    Caches the result for 1 hour to avoid redundant API calls.
    """
    try:
        data = yf.download(ticker, start=start_date, end=end_date)
        if data.empty:
            return None
        return data
    except Exception as e:
        st.error(f"Error fetching data: {e}")
        return None

def compute_technical_indicators(data):
    """
    Compute RSI, MACD, and Bollinger Bands using the 'ta' library.
    Assumes data has standard OHLCV columns.
    """
    df = data.copy()
    
    # Flatten multi-index columns if they exist (yfinance sometimes returns multi-index)
    if isinstance(df.columns, pd.MultiIndex):
        df.columns = df.columns.get_level_values(0)
    
    # Ensure column names are standard
    required_cols = ['Open', 'High', 'Low', 'Close', 'Volume']
    for col in required_cols:
        if col not in df.columns:
            # try finding it case insensitive
            found = False
            for c in df.columns:
                if c.lower() == col.lower():
                    df.rename(columns={c: col}, inplace=True)
                    found = True
                    break
            if not found:
                raise ValueError(f"Missing required column: {col}")

    # RSI
    df['RSI'] = ta.momentum.RSIIndicator(close=df['Close'], window=14).rsi()
    
    # MACD
    macd = ta.trend.MACD(close=df['Close'])
    df['MACD'] = macd.macd()
    df['MACD_Signal'] = macd.macd_signal()
    
    # Bollinger Bands
    bb = ta.volatility.BollingerBands(close=df['Close'], window=20, window_dev=2)
    df['BB_High'] = bb.bollinger_hband()
    df['BB_Low'] = bb.bollinger_lband()
    
    # Drop rows with NaN values generated by indicators
    df.dropna(inplace=True)
    
    return df
